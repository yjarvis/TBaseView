package com.jarvis.tbaseview.model.plugin;
/**
 * 插件协议数据
 * @author tansheng
 */
public class PluginData {
	private String mainbg;//背景图片资源
	private String plugin_home;//主页图片资源
	private String plugin_more;//更多图片资源
	private String plugin_person;//个人图片资源
	
	
	
	public String getMainbg() {
		return mainbg;
	}
	public void setMainbg(String mainbg) {
		this.mainbg = mainbg;
	}
	public String getPlugin_home() {
		return plugin_home;
	}
	public void setPlugin_home(String plugin_home) {
		this.plugin_home = plugin_home;
	}
	public String getPlugin_more() {
		return plugin_more;
	}
	public void setPlugin_more(String plugin_more) {
		this.plugin_more = plugin_more;
	}
	public String getPlugin_person() {
		return plugin_person;
	}
	public void setPlugin_person(String plugin_person) {
		this.plugin_person = plugin_person;
	}


}
